<p class="pagePara">
  L'huître, séparée de son support, est soit mise "en poche", et posée sur des tables en fer placées dans des parcs situés en mer. Ou bien elle est déposée à même le sol sur des parcs 
    dit "à plat", elle va ainsi croître pendant 1 à 2 ans, afin d'obtenir une taille raisonnable pour être commercialisée. <br />
  Vient une étape qui n'est pas obligatoire, l'affinage (en effet certaines huîtres ne sont pas affinées, et sont appelées "pleine mer"). Ce processus a pour principe de mettre les huîtres en "claires", bassins creusés dans des sols imperméables de 300 à 500 m², dont la profondeur n'excède pas 50 cm. L'alimentation en eau est assurée par des canaux, appelés chenaux, qui débouchent sur la mer, prolongés par des "ruissons" ceux ci plus petits. Ces derniers alimentent les claires en eau salée. A chaque printemps, les claires sont vidées  et laissées en Jachère, de plus il faut régulièrement les recreuser (tout les 4/5 ans).<br />
  Ces deux opérations vont favoriser l'apparition de la navicule Bleue. Cette algue unicellulaire en forme de navette est appelée Navicula Ostrearia. Elle est à l'origine de la couleur verte observée au niveau des branchies de l'huître. En effet le pigment bleu de cette algue superposé à la pigmentation de l'huître (un mélange de bleue et de jaune) va donner le vert qui fait la spécificité de l'affinage à Marennes-Oléron.
</p>

