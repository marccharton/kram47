<p class="pagePara" style="clear:both;">
  Dans le bassin de Marennes Oléron, l'huître plate (Ostrea Edulis) était la seule huître exploitée jusqu'au milieu du 19eme siècle. <br />
  Vers 1860, les premiers essais d'implantation de la portugaise (Crassostrea Angulata) furent entrepris. <br /><br />

  En 1868, un navire nommé le Morlaisien, transportant des huîtres portugaises fut contraint par la tempêtes de se réfugier dans l'estuaire de la Gironde. Après plusieurs jours d'attente, la cargaison fut jetée par dessus bord, sur ordre des autorités maritimes. Certaines encore vivantes, se reproduisirent, les courants les emportèrent dans le bassins de Marennes Oléron.<br /><br />
  En 1922, une épizootie anéantit presque totalement les huîtres plates. 
  Progressivement les ostréiculteurs développement l'exploitation de la portugaise jusqu'en 1970. <br /><br />
  Vers 1967 les huîtres, du bassin de Marennes Oléron commencèrent a mourir suite a une nouvelle épizootie. 
  Les pertes se situeraient entre 40 à 50 pourcent du stock.<br /><br />
  En 1970 les pertes touchaient plus de 90 pourcent des huîtres.
  Les causes étaient probablement multiples: salinité trop élevée, fraîcheur anormale de l eau et développement d une maladie virale inconnue. Crassostrea gigas en expérience depuis 1966 dans les locaux de l' Institut Scientifique et Technique des Pèches Maritimes (ISTPM) restait indemne.<br /><br />
  La décision fut prise de remplacer l'huître Portugaise par l' huître d' origine Japonaise. Importée du Canada, de la région de Vancouver, des huîtres mères furent implantées dans les parcs de Marennes Oléron.2700 Tonnes de naissains du Japon furent plongés dans les eaux charentaises permettant ainsi de reconstituer le stock.
</p>

