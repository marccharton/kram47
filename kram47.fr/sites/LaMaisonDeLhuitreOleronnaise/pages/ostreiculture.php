<h1> L'ostréiculture</h1>

<p class="pagePara">
  Pour la zoologie, l’huître est mollusque acéphale, c'est à dire sans tête. Elle appartient au groupe des bivalves. <br />
  La famille, les Ostréidés se distingue par sa coquille plus ou moins feuilletée. <br />
  En France, on rencontre 2 grandes espèces : 
</p>

<p id="pageParaIndent">
  - L'huître plate (Ostrea Edulis L) qui est presque ronde et plate.<br />
  - L'huître creuse, dont le principal représentant est l'huître d'origine Japonaise (Crassostrea Gigas)<br />
  - L'Huître portugaise (  Crassostrea Angulata L) qui possédait de nombreux traits communs avec l'huître japonaise.
</p>

<h2> Les Huitres d'hier et d'aujourd'hui : </h2>

<p class="pagePara" id="pageParaFloat">
  Les chinois semblent être les premiers à avoir cultivé les huîtres. En Europe, l'ostréiculture date de plus de 2000 ans. Les Grecs en mangeaient en abondance, ils les trouvaient sur les bancs naturels. Ils avaient par ailleurs une façon originale d'utiliser les coquilles ainsi Aristide Le Juste en 484 avant J.C fut proscrit d'Athènes à la majorité des huîtres les coquilles ayant servie de bulletins de votes. De cette pratique viendrait le terme d'ostracisme. Chez les romains Sergius Orata fut le premier à installer des parcs dans la région de Baïes. Il fit rapidement fortune car les romains raffolaient des huîtres. A cette époque, on les faisaient venir de toute les régions de l'empire.<br />
  On les Mangeaient crues ou assaisonnées d'une sauce appelée Garum. Un peu plus tard elles étaient présentes sur la tables des grands rois de France : Henri IV, Louis XIV, et Napoléon 1er, qui étaient des grands mangeurs d'huîtres. 
</p>

