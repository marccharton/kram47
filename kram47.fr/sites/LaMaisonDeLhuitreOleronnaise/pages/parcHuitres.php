<h1>  L'estrans et ses particularités </h1>
  
<p class="pagePara">
<b>  Les parcs à huîtres </b> <br />
  La pêche à pied est interdite dans les parcs et à moins de 25 mètres de ceux-ci.<br />
  Mais attention sur Oléron plusieurs endroits complètement différents constituent l'élevage des huîtres. Et cela constitue le "jardin" de l'ostréiculteur.
</p>
  
