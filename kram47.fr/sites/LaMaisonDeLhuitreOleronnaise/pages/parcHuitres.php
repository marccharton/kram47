	<b> Les champs d'huîtres </b>
</div>

<p class="pagePara">  
  Les limites de concessions sont représentées par les pierres qui forme des rectangles séparés par un passage entre deux concessions.<br />
  Les huîtres sont déposées dans ces "champs".<br />
  Marcher dans ses parcs c'est tuer les huîtres présentes, et détruire le travail de l'ostréiculteur!
</p>
  
