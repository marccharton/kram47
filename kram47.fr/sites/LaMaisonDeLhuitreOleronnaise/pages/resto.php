<p class="pagePara">
  En 2002 nous avons installé dans un ancien atelier ostréicole un restaurant, spécialisé dans les produits locaux. Notre objectif est de faire connaître nos huîtres ouvertes et les moules disposées sur une planche, cuites aux aiguilles de pins (spécialité charentaise) : l'EGLADE DE MOULES.
</p>

<img  style="display:inline-block;margin:20 50;" src="assets/pictures/pics/resto.7.gif" width="297" height="299" align="left">

<p class="pagePara" style="width:400px;display:inline-block;">
  La préparation de "l'églade"
  
  En entrée, il est possible de déguster la soupe de crustacés ou la bisque de langoustines, servie avec leurs croûtons et fromage râpé.<br />
  Les huîtres ouvertes à la demande, provenant de nos bassins sont accompagnées du petit vin blanc d'Oléron. Il est possible de choisir entre les fines et les spéciales et d'apprécier ainsi les différences de goûts selon la saison et la provenance des parcs du bassin Marennes-Oléron. <br />
  Le menu enfant, est composé de moules frites, deux boules de glace et d'une boisson au choix.<br />
En dessert, nous vous proposons les fromages de chèvres de notre région, la galette charentaise, le broyé du Poitou, le tourteau fromager et différents parfums de glace<br />
</p>


