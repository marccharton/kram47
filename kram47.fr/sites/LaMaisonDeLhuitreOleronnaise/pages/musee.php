<h1> Espace culturel </h1>

<p class="pagePara">
Lors d'une visite commentée, Patricia et Marc Texier vous feront découvrir les différentes techniques de la culture de l'huître. Installé dans une ancienne cabane ostréicole située au cœur de l'activité huîtrière Oléronaise, celui-ci regroupe une importante collection d'instruments usuels retraçant l'ostréiculture d'hier et d'aujourd'hui, des cartes marines anciennes, des cartes postales du début du siècle, des fossiles d'huîtres de différents pays . Ainsi que des huîtres contemporaines provenant de territoires français d'outre-mer. <br />
La projection d'un film vidéo vous montrera les différentes phases de l'ostréiculture moderne dans le bassin de Marennes Oléron.
</p>

