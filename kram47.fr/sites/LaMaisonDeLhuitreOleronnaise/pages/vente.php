<h2> Le Marché de SAINT TROJAN </h2>


<p class="pagePara" id="pageParaFloat">
  Le Marché couvert de St Trojan, ouvert depuis peu, nous permet de présenter des huîtres issues de notre exploitation.
  La qualité et la fraîcheur sont garanties, nous sortons de l'eau tous les matins huîtres et moules qui seront vendues le jour même.
  Ouvert tous les jours de Pâques à Octobre, et  le Mardi et Samedi, le reste de l'Année, de 8H30 à 13H.
</p>

