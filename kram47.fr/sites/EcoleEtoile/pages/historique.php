<h2> L'ecole l'etoile fete ses 150 ans </h2>

<p class="pagePara" style="display:inline-block;">
    
Un siècle et demi se sont déjà écoulés depuis la création de la 1ère école des "Lazaristesê en Afrique située dans la Médina de Tunis et devenue aujourd'hui "L'Etoileê. <br />
Un événement de taille qui sera célébré sous le patronage du secrétariat d'Etat à la formation professionnelle demain au Théâtre Municipal de Tunis. <br />
Tunis - Le Quotidien Située en pleine médina, l'école primaire "L'Etoileê souffle demain sa 150ème bougie. <br />
Compte tenu de l'importance de l'événement, cet anniversaire sera célébré sous le patronage de M. Mohamed Moncef Ben Sa&#239;d, secrétaire d'Etat auprès du ministre de l'Education nationale et de la Formation chargé de la formation professionnelle. <br />
Une exposition de photos et de documents anciens aura lieu à l'entrée du Théâtre Municipal demain à partir de 15h. <br />
La soirée sera marquée par un gala dans les mêmes lieux qui sera animé par une pléiade d'artistes en herbe et inscrits dans l'école. <br />
L'itinéraire de cette institution est chargé en émotions et riche en événements. <br />
C'est en 1635 que fut construite la chapelle Sainte Croix à l'époque des frères Auguet Pierre, située aujourd'hui dans l'actuel emplacement de l'école, en plein milieu de la médina. <br />
Ce n'est que quelques années plus tard, en effet, que les premières pierres pour la construction de l'école chrétienne, la première d'Afrique d'après certaines informations, furent posées. <br />
Les livres d'histoire n'ont pas tari d'éloges sur l'importance de cet édifice. <br />
"Les congrégations religieuses masculinesê, "Les souksê n'ont pas manqué de mettre en valeur les richesses historiques de ces lieux bénéficiant des mêmes prérogatives que la médina de Tunis dans le cadre du statut accordé par l'Unesco. <br />
Mme Ramla Chamli, fille de feu Abdelhakim Chemli, illustre éducateur qui a donné à l'école son cachet arabo-musulman, déclare toute son émotion à l'occasion de cet événement ainsi que sa détermination à suivre la voie tracée par son père, celle de l'ouverture et de l'authenticité. <br />
    
